int		ft_iterative_factorial(int nb)
{
	int result;

	result = 1;
	if ((nb < 0) || nb > 12)
	{
		return (0);
	}
	else if (nb == 0 || nb == 1)
	{
		return (1);
	}
	else
	{
		while (nb >= 1)
		{
			result = nb * result;
			nb--;
		}
	}
	return (result);
}
